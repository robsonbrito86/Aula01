class Calculadora:
    def __init__(self, somar, subtrair, multiplicar, dividir):
        self.somar=somar
        self.subtrair=subtrair
        self.multiplicar=multiplicar
        self.dividir=dividir
    
    def somar(self, a, b):
        return a + b
        pass
    
    
    def subtrair(self, a, b):
        return a - b
        pass
    
    def multiplicar(self, a, b):
        return a * b
        pass
    
    def dividir(self, a, b):
        return a == b
        pass
    

    


        